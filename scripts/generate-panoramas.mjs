//
// Each filename encodes the geographic + temporal metadata:
//   YEAR_LAT_LNG_slug.jpg   (YEAR is negative for BC, LAT/LNG may be negative)
//
// Run with:  node scripts/generate-panoramas.mjs
import { readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const PANO_DIR = join(ROOT, "public", "panoramas");
const OUT_FILE = join(ROOT, "src", "data", "panoramas.ts");

const FILENAME_RE = /^(-?\d+)_(-?\d+(?:\.\d+)?)_(-?\d+(?:\.\d+)?)_(.+)\.jpe?g$/i;

// Humanize a slug like "genghis-khan-s-death" -> "Genghis Khan's Death"
function humanize(slug) {
  return slug
    .replace(/-s-/g, "'s ")          // possessive: "khan-s-" -> "khan's "
    .replace(/-/g, " ")
    .split(" ")
    .map((w) => (w ? w[0].toUpperCase() + w.slice(1) : w))
    .join(" ")
    .trim();
}

function centuryLabel(year) {
  const era = year < 0 ? "Ã®.Hr." : "d.Hr.";
  const abs = Math.abs(year);
  const century = Math.floor((abs - 1) / 100) + 1;
  return `secolul al ${century}-lea ${era}`;
}

function buildClues(year, lat, lng) {
  return [
    `Evenimentul s-a petrecut Ã®n ${centuryLabel(year)}`,
    `Locul se aflÄƒ Ã®n emisfera ${lat >= 0 ? "nordicÄƒ" : "sudicÄƒ"}.`,
    `Coordonatele indicÄƒ ${lng >= 0 ? "estul" : "vestul"} meridianului Greenwich.`,
  ];
}

function buildDescription(title, year, lat, lng) {
  const era = year < 0 ? `${Math.abs(year)} Ã®.Hr.` : `anul ${year}`;
  return `${title} â€” panoramÄƒ istoricÄƒ de 360Â°, surprinsÄƒ Ã®n ${era}, Ã®n apropierea coordonatelor ${lat.toFixed(4)}, ${lng.toFixed(4)}.`;
}

const files = readdirSync(PANO_DIR)
  .filter((f) => /\.jpe?g$/i.test(f))
  .sort();

const entries = [];
for (const file of files) {
  const m = file.match(FILENAME_RE);
  if (!m) {
    console.warn(`Skipping unrecognized filename: ${file}`);
    continue;
  }
  const year = parseInt(m[1], 10);
  const lat = parseFloat(m[2]);
  const lng = parseFloat(m[3]);
  const title = humanize(m[4]);
  entries.push({
    id: `pano-${entries.length + 1}`,
    title,
    year,
    lat,
    lng,
    imageUrl: `/panoramas/${file}`,
    is360: true,
    description: buildDescription(title, year, lat, lng),
    clues: buildClues(year, lat, lng),
  });
}

const years = entries.map((e) => e.year);
const minYear = Math.min(...years);
const maxYear = Math.max(...years);

const header = `// AUTO-GENERATED by scripts/generate-panoramas.mjs â€” do not edit by hand.
// Source of truth is the filenames in public/panoramas/ (YEAR_LAT_LNG_slug.jpg).
import type { HistoricalLocation } from "./locations";

`;

const body =
  `export const panoramaLocations: HistoricalLocation[] = ${JSON.stringify(entries, null, 2)};\n\n` +
  `// Temporal span of the panorama pool, used to scale the timeline slider.\n` +
  `export const PANORAMA_MIN_YEAR = ${minYear};\n` +
  `export const PANORAMA_MAX_YEAR = ${maxYear};\n`;

writeFileSync(OUT_FILE, header + body);
console.log(`Wrote ${entries.length} panoramas to ${OUT_FILE} (years ${minYear}..${maxYear}).`);
